u16 ADC_ConvertedValue[ADC_SAMPLE_NUM][ADC_FINAL_ENUM];
u16 ADC_Distance[4];
float Comp_Distance[4];
u16 ADC_Max_Value[4]={1500,1500,1500,1500};
u16 ADC_Min_Value[4]={27,27,27,27};
u32 ADC_Sample_times;
float BatteryPercentage=100;
float BatteryPercentageTemp;
float BatteryVoltage;
void ADC_Callback1(ADC_HandleTypeDef* hadc1) 
{
    u32 i;
    s32 temp1,temp2;
    float DistancePercentage;
    ADC_ConvertedValueType j;
     ADC_Sample_times++;
    for(j=0;j<=ADC_RIGHT_LED;j++)
    {
        u32 total=0;
        for(i=0;i<ADC_SAMPLE_NUM;i++)
        {
            total+=ADC_ConvertedValue[i][j];
        }
        ADC_Distance[j]=~(total >> ADC_SAMPLE_ODR)&0x0fff;
        /*百分化处理*/
		temp1=100*(ADC_Distance[j]-ADC_Min_Value[j]);
		if(temp1<0)temp1=0;
		temp2=ADC_Max_Value[j]-ADC_Min_Value[j];
		DistancePercentage=(float)temp1/temp2;
        if(DistancePercentage>5)
        {
            Comp_Distance[j]=DistancePercentage;
        }
        else
        {
            Comp_Distance[j]=0;
        }
    }
}
void ADC_Callback2(ADC_HandleTypeDef* hadc1) 
{
    BatteryVoltage = (float)ADC_ConvertedValue[0][ADC_BATTERY]*2.f/4096.f*3.3f;
    BatteryPercentageTemp =  (BatteryVoltage - CHARGE_VOLTAGE)/(CHG_CPLT_VLTGf-CHARGE_VOLTAGE)*100;
    BatteryPercentage = BatteryPercentage*0.992f+0.008f*BatteryPercentageTemp;
}
void ADC_Distance_Calib()
{
	u8 i=0;
    memset(ADC_Max_Value, 0, sizeof(ADC_Max_Value));
    memset(ADC_Min_Value, 0Xffff, sizeof(ADC_Min_Value));
    /*查找最大值最小值*/
	for(i=0;i<4;i++)
	{
		if(ADC_Distance[i]>ADC_Max_Value[i])
		{
			ADC_Max_Value[i]=ADC_Distance[i];
        }
		if(ADC_Distance[i]<ADC_Min_Value[i])
		{
			ADC_Min_Value[i]=ADC_Distance[i];
        }
    }
    
}
